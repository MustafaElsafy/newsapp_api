    public function getToken(Request $request)
    {
        $request->validate([
            'email' => 'required',
            'password' => 'required'
        ]);
        $credentials = $request->only('email', 'password');
        if (Auth::attempt($credentials)) {
            $user = User::where('email', $request->get('email'))->first();
            return new TokenResource(['token' => $user->api_token]);
        } else {
            return 'not found';
        }
    }
